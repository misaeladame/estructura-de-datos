    private void btnReacomodoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReacomodoActionPerformed
        Alumno[] grupoSel = getGrupoSel();
        int noGrupoSel = cbGrupo.getSelectedIndex(); 
        if(noAlusGpo[noGrupoSel] > 1) {
            Alumno[] mujeres = new Alumno[noAlusGpo[noGrupoSel]];
            Alumno[] hombres = new Alumno[noAlusGpo[noGrupoSel]];
            for(int i=0;i < noAlusGpo[noGrupoSel];i++) {
                mujeres[i] = new Alumno();
                hombres[i] = new Alumno();
            }
            int noMujeres = 0, noHombres = 0;
            for(int i=0;i < noAlusGpo[noGrupoSel];i++)
                if (grupoSel[i].getSexo() == 'F')
                    mujeres[noMujeres++].Copia(grupoSel[i]);
                else
                    hombres[noHombres++].Copia(grupoSel[i]);
            int indiceGrupoSel = 0;
            for(int i=0;i < noMujeres;i++)
                grupoSel[indiceGrupoSel++].Copia(mujeres[i]);
            for(int i=0;i < noHombres;i++)
                grupoSel[indiceGrupoSel++].Copia(hombres[i]);
        }
        JOptionPane.showMessageDialog(this, 
                "SE HA REALIZADO EL REACOMODO ...",
                "Ejercicio (H)", JOptionPane.PLAIN_MESSAGE); 
    }//GEN-LAST:event_btnReacomodoActionPerformed

