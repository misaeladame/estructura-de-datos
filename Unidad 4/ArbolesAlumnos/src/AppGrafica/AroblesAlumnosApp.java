    private void SumaAprob(Nodo p,int[] sumaAprob) {
        if (p!=null) {
            SumaAprob(p.Izq(),sumaAprob);
            SumaAprob(p.Der(),sumaAprob);
            sumaAprob[0] += p.Info().Calif() >= 70? p.Info().Calif() : 0;
        }
    }
    private void btnPromAprobActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPromAprobActionPerformed
        Arbol grupoSel = rb1.isSelected()? grupo1 : grupo2;
        int[] noAprob = {0};
        NoAprob(grupoSel.Raiz(),noAprob);
        if (noAprob[0] == 0)
            JOptionPane.showMessageDialog(this, 
                "NO HAY APROBADOS, DADO QUE ESTÁN EN EL GRUPO DE UN SERVIDOR", 
                    "RESULTADO ...", 
                    JOptionPane.PLAIN_MESSAGE);
        else {
            int[] sumaAprob = {0};
            SumaAprob(grupoSel.Raiz(),sumaAprob);
            JOptionPane.showMessageDialog(this, 
                "PROMEDIO APROBADOS = "+(double)sumaAprob[0]/noAprob[0], 
                    "RESULTADO DEL PROMEDIO ...", 
                    JOptionPane.PLAIN_MESSAGE);
        } 
    }//GEN-LAST:event_btnPromAprobActionPerformed

