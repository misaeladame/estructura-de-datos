    private void eliReprobados(Alumno[] grupo,int col) {
        if (noAlusGpo[col] > 0) {
            Alumno[] aux = new Alumno[noAlusGpo[col]];
            for(int i=0;i<noAlusGpo[col];i++)
                aux[i] = new Alumno();
            int noAprob = 0;
            for(int i=0;i<noAlusGpo[col];i++) 
                if (grupo[i].getCalif() >= 70)
                    aux[noAprob++].Copia(grupo[i]);
            for(int i=0;i < noAprob;i++) 
                grupo[i].Copia(aux[i]);
            noAlusGpo[col] = noAprob;
            tblNoAlusGpo.setValueAt(noAlusGpo[col],0,col);
        }
    }
