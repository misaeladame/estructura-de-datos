    private void Agregar(Arbol grupo) throws IllegalAccessException {
        if (Integer.parseInt(tblLeerAlu.getValueAt(0, 2).toString()) > 100)
            throw new IllegalAccessException("LA CALIFICACION NO PUEDE SER MAYOR QUE 100");
        grupo.Add(new TipoElem(tblLeerAlu.getValueAt(0, 0).toString(),
                        tblLeerAlu.getValueAt(0, 1).toString(),
                        Integer.parseInt(tblLeerAlu.getValueAt(0, 2).toString())));
    }
