    private void btnVisuaGrupoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVisuaGrupoActionPerformed
        ArrayList<Alumno> grupoSel = getGrupoSel();
        lblNoGpoVisua.setText("GRUPO "+
                              Integer.toString(cbGrupo.getSelectedIndex()+1));
        DefaultTableModel modelo=(DefaultTableModel) tblVisuaGrupo.getModel();
        while (tblVisuaGrupo.getRowCount() > 0)
            modelo.removeRow(0);
        int i = 0;
        for(Alumno alu : grupoSel) {
            Object[] o = new Object[5];
            o[0] = ++i;
            o[1] = alu.getNoCon();
            o[2] = alu.getNombre();
            o[3] = alu.getSexo();
            o[4] = alu.getCalif();
            modelo.addRow(o);
        }
    }//GEN-LAST:event_btnVisuaGrupoActionPerformed

