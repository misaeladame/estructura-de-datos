    private ArrayList<Mascota> getGrupoSel() {
        int noGrupoSel = jComboGrupo.getSelectedIndex();
        switch(noGrupoSel){
            case 0:
                return grupo1;
            case 1:
                return grupo2;
            default:
                return grupo1;
        }
    }
    
    private void eliminarPorCosto(ArrayList<Mascota> grupo){
        for (int i = 0; i < grupo.size(); i++){
            if(grupo.get(i).getCostoVacunas() > 100){
                grupo.remove(i);
            }
        }
    }
    
    private void jButtAnadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtAnadirActionPerformed
        // TODO add your handling code here:
        ArrayList<Mascota> grupoSel = getGrupoSel();
        Mascota masc = new Mascota();
        masc.Leer(jTextNombre.getText(), jTextTipo.getText(), 
                  Integer.parseInt(jTextNoVacunas.getText()),
                  Double.parseDouble(jTextCosto.getText()), jRadioM.isSelected() ? 'M' : 'F',   
                  Integer.parseInt(jTextEdad.getText()));
        grupoSel.add(masc);
        jTabNoMasc.setValueAt(grupoSel.size(), 0, jComboGrupo.getSelectedIndex());
    }//GEN-LAST:event_jButtAnadirActionPerformed

