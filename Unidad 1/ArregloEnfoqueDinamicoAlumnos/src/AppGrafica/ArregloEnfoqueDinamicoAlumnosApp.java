    private void btnReacomodoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReacomodoActionPerformed
        ArrayList<Alumno> grupoSel = getGrupoSel();
        ArrayList<Alumno> aux = new ArrayList<>();
        for(int i=0;i<grupoSel.size();i++) {
            Alumno alu = grupoSel.get(i);
            if (alu.getSexo()=='M') {
                grupoSel.remove(i);
                --i;
                aux.add(alu);
            }
        }
        for(Alumno alu : aux)
            grupoSel.add(alu);
        JOptionPane.showMessageDialog(this, 
                "SE HA REALIZADO EL REACOMODO ...",
                "Ejercicio (H)", JOptionPane.PLAIN_MESSAGE);
    }//GEN-LAST:event_btnReacomodoActionPerformed

