    private void jButtVisGpoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtVisGpoActionPerformed
        // TODO add your handling code here:
        ArrayList<Mascota> grupoSel = getGrupoSel();
        jLabGrupoVis.setText("Grupo " + Integer.toString(jComboGrupo.getSelectedIndex() + 1));
        DefaultTableModel modelo = (DefaultTableModel) jTabMascotas.getModel();
        modelo.setRowCount(0);
        int i = 0;
        for(Mascota masc : grupoSel){
            Object[] o = new Object[7];
            o[0] = ++i;
            o[1] = masc.getNombre();
            o[2] = masc.getTipo();
            o[3] = masc.getNoVacunas();
            o[4] = masc.getCostoVacunas();
            o[5] = masc.getSexo();
            o[6] = masc.getEdad();
            modelo.addRow(o);
        }
    }//GEN-LAST:event_jButtVisGpoActionPerformed

    private void jButtIntercambiar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtIntercambiar1ActionPerformed
        // TODO add your handling code here:
        ArrayList<Mascota> grupoSel = getGrupoSel();
        Mascota aux = grupoSel.get(0);
        grupoSel.set(0, grupoSel.get(grupoSel.size() - 1));
        grupoSel.set(grupoSel.size() - 1, aux);
    }//GEN-LAST:event_jButtIntercambiar1ActionPerformed

