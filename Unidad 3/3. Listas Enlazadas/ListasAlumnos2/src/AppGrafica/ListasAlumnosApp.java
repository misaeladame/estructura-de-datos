    private void btnInvertirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInvertirActionPerformed
         Lista grupoSel = rb1.isSelected()? grupo1 : grupo2;
         Lista aux = new Lista();
         Nodo p = grupoSel.Cab();
         while (p != null) {
             aux.InsEnd(p.Info());
             p = p.Sig();
         }
         grupoSel.Clear();
         p = aux.Cab();
         while (p != null) {
             grupoSel.InsBegin(p.Info());
             p = p.Sig();
         }
    }//GEN-LAST:event_btnInvertirActionPerformed

    private void btnEliTodosMenosUltActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliTodosMenosUltActionPerformed
        Lista grupoSel = rb1.isSelected()? grupo1 : grupo2;
        if(grupoSel.Length() > 1) {
            while (grupoSel.Cab().Sig() != null)
                grupoSel.Cab(grupoSel.Cab().Sig());
            grupoSel.Length(1);
        }
    }//GEN-LAST:event_btnEliTodosMenosUltActionPerformed

    private void btnEliTodosMenosUltAuxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliTodosMenosUltAuxActionPerformed
        Lista grupoSel = rb1.isSelected()? grupo1 : grupo2;
        if(grupoSel.Length() > 1) {
            Lista aux = new Lista();
            Nodo p = grupoSel.Cab();
            while (p.Sig() != null) 
                p = p.Sig();
            aux.InsEnd(p.Info());
            grupoSel.Clear();
            grupoSel.InsEnd(aux.Cab().Info());
        }
    }//GEN-LAST:event_btnEliTodosMenosUltAuxActionPerformed

