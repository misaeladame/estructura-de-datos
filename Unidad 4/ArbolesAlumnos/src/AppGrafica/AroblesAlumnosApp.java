    private void NoAprob(Nodo p,int[] noAprob) {
        if (p!=null) {
            NoAprob(p.Izq(),noAprob);
            noAprob[0] += p.Info().Calif() >= 70? 1 : 0;    //inorden
            NoAprob(p.Der(),noAprob);       
        }
    }
