    private void Visua(TipoElem elem) {
        DefaultTableModel modelo = (DefaultTableModel) tblVisuaGpo.getModel();
        Object[] o = new Object[4];
        o[0] = modelo.getRowCount()+1;
        o[1] = elem.NoCon();
        o[2] = elem.Nombre();
        o[3] = elem.Calif();
        modelo.addRow(o);
    }
    private void VisuaRep(Nodo p) {
        if (p != null) {
            if(p.Info().Calif() < 70) 
                Visua(p.Info());
            VisuaRep(p.Izq());
            VisuaRep(p.Der());
        }
    }
    private void btnVisuaRepActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVisuaRepActionPerformed
        Arbol grupoSel = rb1.isSelected()? grupo1 : grupo2;
        DefaultTableModel modelo = (DefaultTableModel) tblVisuaGpo.getModel();
        modelo.setRowCount(0);
        VisuaRep(grupoSel.Raiz());
    }//GEN-LAST:event_btnVisuaRepActionPerformed

    private void NoNodos(Nodo p, int[] noNodos) {
        if(p == null)
            return;
        else {
            NoNodos(p.Izq(), noNodos);  
            NoNodos(p.Der(), noNodos);
            noNodos[0]++;
        }
            
    }
    
