    private void VisuaAmigo(Persona amigo, String idAmigo) {
        lblAmigo.setText(idAmigo);
        lblNombre.setText(amigo.getNombre());
        lblEdad.setText(Integer.toString(amigo.getEdad()));
        lblSexo.setText(String.valueOf(amigo.getSexo()));
        lblCorreo.setText(amigo.getCorreo());
    }
    
