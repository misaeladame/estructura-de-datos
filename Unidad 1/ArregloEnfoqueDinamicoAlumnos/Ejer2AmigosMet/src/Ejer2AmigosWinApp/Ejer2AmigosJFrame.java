    private void LeerAmigo(Persona amigo) {
        amigo.setNombre(txfNombre.getText());
        amigo.setEdad(Integer.parseInt(txfEdad.getText()));
        amigo.setSexo(txfSexo.getText().charAt(0));
        amigo.setCorreo(txfCorreo.getText());
    }
    
